import configparser


def read_config():
    global win_width, win_height, button_width, button_height, size, max_kills, fontsize
    config = configparser.ConfigParser()
    config.read("settings.ini")
    win_width = int(config.get('Settings', 'win_width'))
    win_height = int(config.get('Settings', 'win_height'))
    button_width = int(config.get('Settings', 'button_width'))
    button_height = int(config.get('Settings', 'button_height'))
    size = float(config.get('Settings', 'size'))
    max_kills = int(config.get('Settings', 'max_kills'))
    fontsize = int(config.get('Settings', 'fontsize'))


def change_config(ww, wh, bw, bh, s, mk, fs):
    config = configparser.ConfigParser()
    config.add_section("Settings")
    config.set("Settings", "win_width", str(ww))
    config.set("Settings", "win_height", str(wh))
    config.set("Settings", "button_width", str(bw))
    config.set("Settings", "button_height", str(bh))
    config.set("Settings", "size", str(s))
    config.set("Settings", "max_kills", str(mk))
    config.set("Settings", "fontsize", str(fs))

    with open("settings.ini", "w") as config_file:
        config.write(config_file)


